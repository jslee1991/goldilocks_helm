--###################################################
--# CREATE TABLE: with <default clause>
--###################################################


--# result: success
CREATE TABLE t1 
( 
    id     NUMBER        DEFAULT 0 
  , name   VARCHAR(128)  DEFAULT 'anonymous'
  , addr   VARCHAR(1024) DEFAULT NULL 
);
COMMIT;



--# result: 1 row
INSERT INTO t1 DEFAULT VALUES;

--# result: 1 row
INSERT INTO t1 VALUES( 1, DEFAULT, 'anywhere' );

--# result: 1 row
INSERT INTO t1 VALUES( 2, 'someone', 'somewhere' );
COMMIT;



--# result: 3 rows
--#        0    anonymous   null
--#        1    anonymous   anywhere
--#        2    someone     somewhere
SELECT * FROM t1 ORDER BY id;



--# result: success
DROP TABLE t1;
COMMIT;



--###################################################
--# CREATE TABLE: with <identity column specification>
--###################################################


--# result: success
CREATE TABLE t1 
( 
    id     NUMBER        GENERATED BY DEFAULT AS IDENTITY 
  , name   VARCHAR(128)
  , addr   VARCHAR(1024) 
);
COMMIT;



--# result: 1 row
INSERT INTO t1 DEFAULT VALUES;

--# result: 1 row
INSERT INTO t1 VALUES( DEFAULT, 'anyone', 'anywhere' );

--# result: 1 row
INSERT INTO t1 VALUES( DEFAULT, 'someone', 'somewhere' );
COMMIT;



--# result: 3 rows
--#        1    null        null
--#        2    anyone      anywhere
--#        3    someone     somewhere
SELECT * FROM t1 ORDER BY id;



--# result: success
DROP TABLE t1;
COMMIT;


