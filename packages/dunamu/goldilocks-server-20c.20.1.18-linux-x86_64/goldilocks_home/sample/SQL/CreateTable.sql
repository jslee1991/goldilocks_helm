--###################################################
--# CREATE TABLE: simple example
--###################################################

--# result: success
DROP TABLE IF EXISTS t1;
COMMIT;

--# result: success
CREATE TABLE t1 
( 
    id     NUMBER
  , name   VARCHAR(128) 
);
COMMIT;


--# result: 1 row
INSERT INTO t1 VALUES ( 1, 'anonymous' );

--# result: 1 row
INSERT INTO t1 VALUES ( 2, 'someone' );
COMMIT;

--# result: 2 rows
--#        1   anonymous
--#        2   someone
SELECT * FROM t1 ORDER BY id;



--# result: success
DROP TABLE t1;
COMMIT;



