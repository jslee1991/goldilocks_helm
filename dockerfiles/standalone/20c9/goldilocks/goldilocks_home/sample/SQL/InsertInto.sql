--###################################################
--# INSERT: with <DEFAULT>
--###################################################

--# result: success
DROP TABLE IF EXISTS t1;
COMMIT;

--# result: success
CREATE TABLE t1 
( 
    id     NUMBER         GENERATED BY DEFAULT AS IDENTITY
  , name   VARCHAR(128)   DEFAULT 'N/A'
  , addr   VARCHAR(1024)  DEFAULT 'N/A'
);
COMMIT;





--# result: 1 row
INSERT INTO t1 VALUES ( DEFAULT, 'anonymous', 'unknown' );
COMMIT;


--# result: 1 row
INSERT INTO t1 VALUES ( 2, DEFAULT, 'somewhere' );
COMMIT;


--# result: 1 row
INSERT INTO t1 VALUES ( 3, 'leekmo', DEFAULT );
COMMIT;


--# result: 3 rows
--#      1    anonymous   unknown
--#      2    N/A         somewhere
--#      3    leekmo      N/A
SELECT * FROM t1;



--# result: success
DROP TABLE t1;
COMMIT;



