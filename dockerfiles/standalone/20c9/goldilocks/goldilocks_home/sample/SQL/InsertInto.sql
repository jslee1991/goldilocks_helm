                      ( 2,   'someone', 'somewhere' ),
                      ( 3,    'leekmo', 'leekmo''s home' );
COMMIT;


--# result: success
CREATE TABLE t2 
( 
    id     NUMBER
  , name   VARCHAR(128) 
);
COMMIT;


--# 3 rows
INSERT INTO t2( id, name ) 
       SELECT id, name FROM t1;
COMMIT;


